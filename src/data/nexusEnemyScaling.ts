export interface NexusTierRow {
  tier: number;
  physMin: number;
  physMax: number;
  elementalMin: number;
  elementalMax: number;
  chaosMin: number;
  chaosMax: number;
  health: number;
  attacksPerSecond: number;
  accuracy: number;
  evasion: number;
  armour: number;
  elementalResPercent: number;
  chaosResPercent: number;
  physDps: number;
  eleDps: number;
  chaosDps: number;
}

function n(
  tier: number,
  physMin: number,
  physMax: number,
  eleMin: number,
  eleMax: number,
  chaosMin: number,
  chaosMax: number,
  health: number,
  aps: number,
  acc: number,
  eva: number,
  arm: number,
  eleRes: number,
  chaosRes: number,
  physDps: number,
  eleDps: number,
  chaosDps: number
): NexusTierRow {
  return {
    tier,
    physMin,
    physMax,
    elementalMin: eleMin,
    elementalMax: eleMax,
    chaosMin,
    chaosMax,
    health,
    attacksPerSecond: aps,
    accuracy: acc,
    evasion: eva,
    armour: arm,
    elementalResPercent: eleRes,
    chaosResPercent: chaosRes,
    physDps,
    eleDps,
    chaosDps,
  };
}

